            str = str.split('').reverse().join('');
            var tg = "";
            var i = 0;
            for (i; i < str.length; i++) {
                tg += str[i];
                if (i !== str.length - 1 && (i + 1) % 3 === 0) {
                    tg += '.';
                }
            }
            return tg.split('').reverse().join('');
